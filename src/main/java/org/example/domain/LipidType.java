package org.example.domain;

/**
 * Enum class which defines the types of lipids that can be used in the program. Each lipid is defined in shorthand form.
 */
public enum LipidType {
    /**
     * Cholesteryl esters, CE
     */
    CE,

    /**
     * Diglycerides, DG
     */
    DG,

    /**
     * Monoglycerides, MG
     */
    MG,

    /**
     * Phosphatidic acids, PA
     */
    PA,

    /**
     * Phosphatidylcholines, PC
     */
    PC,

    /**
     * Phosphatidylethanolamines, PE
     */
    PE,

    /**
     * Phosphatidylinositols, PI
     */
    PI,

    /**
     * Phosphatidylgylcerols, PG
     */
    PG,

    /**
     * Phosphatidylserines, PS
     */
    PS,

    /**
     * Triglycerides, TG
     */
    TG,

    /**
     * Cardiolipins, CL
     */
    CL
}
